using UnityEngine;
using System.Collections;

public class Geometry : MonoBehaviour
{
    public static Vector3 MultiplyVectorFields(Vector3 a, Vector3 b)
    {
        return new Vector3(a.x * b.x, a.y * b.y, a.z * b.z);
    }

    public static Vector3 DivideVectorFields(Vector3 a, Vector3 b)
    {
        return new Vector3(a.x / b.x, a.y / b.y, a.z / b.z);
    }
}
